package com.Tap.package1;

public class Day7 {

	public static void main(String[] args) {
		int n=4;
		//Entering into the Row
		for(int i=1;i<=n;i++) {
			//Entering into the column
			for(int j=1;j<=n;j++) {
				System.out.print("* ");
			}
			//exiting the row
			System.out.println();
			
			
		}

	}

}
